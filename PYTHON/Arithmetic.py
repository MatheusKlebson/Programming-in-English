first_number = int(input("Number First: "))
second_number = int(input("Number Second: "))
print(f"{first_number} + {second_number} = {first_number + second_number}")
print(f"{first_number} - {second_number} = {first_number - second_number}")
print(f"{first_number} / {second_number} = {first_number / second_number}")
print(f"{first_number} x {second_number} = {first_number * second_number}")
print(f"{first_number} % {second_number} = {first_number % second_number}")
print(f"{first_number} // {second_number} = {first_number // second_number}")
print(f"{first_number} ** {second_number} = {first_number ** second_number}")